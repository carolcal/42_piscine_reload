void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	if (*str != '\0')
	{
		ft_putchar(*str);
		str++;
		ft_putstr(str);
	}
}

int	main(int argc, char*argv[])
{
	int	i;

	i = 1;
	while (i < argc)
	{
		ft_putstr(argv[i]);
		ft_putchar('\n');
		i++;
	}
	return (0);
}
